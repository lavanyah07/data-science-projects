def make_modern_lines(n):
    lines = []
    domain_samples = []
    for d, examples in domains.items():
        domain_samples.extend([(d, ex) for ex in examples])
    while len(lines) < n:
        if random.random() < 0.4 and domain_samples:
            d, ex = random.choice(domain_samples)
            if random.random() < 0.5:
                ex2 = ex.replace("you are", "you're").replace("you have", "you've")
            else:
                ex2 = ex
            lines.append((d, ex2))
        else:
            d = random.choice(list(domains.keys()))
            s = random.choice(extra_subjects)
            v = random.choice(extra_verbs)
            o = random.choice(extra_objects)
            punct = random.choice([".", "?", "."])
            sentence = f"{s} {v} {o}{punct}"
            lines.append((d, sentence))
    return lines

def modern_to_archaic(s):
    s = s.strip()
    repl = {
        r"\bYou\b": "Thou",
        r"\byou\b": "thou",
        r"\byour\b": "thy",
        r"\bYou've\b": "Thou hast",
        r"\byou've\b": "thou hast",
        r"\bare\b": "art",
        r"\bdo\b": "dost",
        r"\bdid\b": "didst",
        r"\bhave\b": "hast",
        r"\bhas\b": "hath",
        r"\bwill\b": "wilt",
        r"\bwould\b": "wouldst",
        r"\bcan\b": "canst",
        r"\bcannot\b": "canst not",
        r"\bnot\b": "naught",
        r"\band\b": "an",
        r"\bfor\b": "forsooth",
        r"\bplease\b": "prithee",
        r"\bThe\b": "Ye",
        r"\bthe\b": "ye",
    }
    for pat, rep in repl.items():
        s = re.sub(pat, rep, s)
    s = re.sub(r"\b(\w{4,})s\b", r"\1eth", s)
    if random.random() < 0.18:
        s = "Verily, " + s
    if random.random() < 0.1:
        s = "Prithee, " + s[0].lower() + s[1:]
    s = re.sub(r"\s+", " ", s)
    s = s.replace(" .", ".").replace(" ,", ",")
    return s

