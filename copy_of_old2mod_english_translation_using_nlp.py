"""Preprocessing & tokenization"""

# CELL 5 — preprocessing
def preprocess_function(batch):
    inputs = [f"modernize [{d}]: {s}" for d,s in zip(batch["domain"], batch["src_old"])]
    model_inputs = tokenizer(inputs, max_length=MAX_SOURCE_LENGTH, truncation=True, padding="max_length")
    labels = tokenizer(batch["tgt_modern"], max_length=MAX_TARGET_LENGTH, truncation=True, padding="max_length")
    label_ids = labels["input_ids"]
    # replace pad token id's in labels by -100 so loss ignores them
    label_ids = [[(l if l != tokenizer.pad_token_id else -100) for l in lab] for lab in label_ids]
    model_inputs["labels"] = label_ids
    return model_inputs

train_tok = train_ds.map(preprocess_function, batched=True, remove_columns=train_ds.column_names)
test_tok  = test_ds.map(preprocess_function, batched=True, remove_columns=test_ds.column_names)

from transformers import DataCollatorForSeq2Seq
data_collator = DataCollatorForSeq2Seq(tokenizer, model=model)

"""Training with auto-resume & checkpointing"""

# CELL 6 — auto-resume training
import os, numpy as np
from transformers import Seq2SeqTrainingArguments, Seq2SeqTrainer
import evaluate

# metrics
bleu = evaluate.load("sacrebleu")
chrf = evaluate.load("chrf")
def postprocess_texts(preds, labels):
    preds = [p.strip() for p in preds]
    labels = [l.strip() for l in labels]
    return preds, labels

def compute_metrics(eval_pred):
    preds_ids, labels_ids = eval_pred
    preds = tokenizer.batch_decode(preds_ids, skip_special_tokens=True)
    labels_ids = np.where(labels_ids != -100, labels_ids, tokenizer.pad_token_id)
    labels = tokenizer.batch_decode(labels_ids, skip_special_tokens=True)
    preds, labels = postprocess_texts(preds, labels)
    bleu_score = bleu.compute(predictions=preds, references=[[l] for l in labels])["score"]
    chrf_score = chrf.compute(predictions=preds, references=labels)["score"]
    return {"bleu": bleu_score, "chrf": chrf_score}

# find last checkpoint (if exists)
last_checkpoint = None
if os.path.isdir(OUTPUT_DIR):
    chks = [d for d in os.listdir(OUTPUT_DIR) if d.startswith("checkpoint-")]
    if chks:
        chks = sorted(chks, key=lambda x: int(x.split("-")[-1]))
        last_checkpoint = os.path.join(OUTPUT_DIR, chks[-1])
        print("Found checkpoint:", last_checkpoint)
    else:
        print("No checkpoint found in", OUTPUT_DIR)
else:
    print("Output dir does not exist; will create", OUTPUT_DIR)

training_args = Seq2SeqTrainingArguments(
    output_dir=OUTPUT_DIR,
    eval_strategy="epoch",
    save_strategy="epoch",       # save after every epoch
    logging_strategy="steps",
    logging_steps=50,
    learning_rate=3e-4,
    per_device_train_batch_size=8,
    per_device_eval_batch_size=8,
    weight_decay=0.01,
    save_total_limit=5,
    num_train_epochs=8,         # total epochs you want
    predict_with_generate=True,
    fp16=False,                 # set True if running on GPU with fp16 support
    load_best_model_at_end=False,
    report_to="none"            # Disable wandb reporting
)

trainer = Seq2SeqTrainer(
    model=model,
    args=training_args,
    train_dataset=train_tok,
    eval_dataset=test_tok,
    tokenizer=tokenizer,
    data_collator=data_collator,
    compute_metrics=compute_metrics
)

# start or resume
if last_checkpoint:
    print("Resuming training from checkpoint:", last_checkpoint)
    trainer.train(resume_from_checkpoint=last_checkpoint)
else:
    print("Starting training from scratch.")
    trainer.train()

# save final model & tokenizer
trainer.save_model(OUTPUT_DIR)
tokenizer.save_pretrained(OUTPUT_DIR)
